//Utilize if/else para escrever um código que defina três números em variáveis e retorne true se pelo menos uma das três for par. Caso contrário, o código deve retornar false.Bônus: use somente um if.
let a = 49;
let b = 62;
let c = 71;
if (a % 2 === 0 || b % 2 === 0 || c % 2 === 0) {
    console.log(true);
}
else{
    console.log(false);
}
